        });

        // Show the appropriate button on hover based on video's play/pause state
        playPauseButton.addEventListener('mouseenter', function() {
            if (videoPlayer.paused) {
                playIcon.style.display = 'block';
                pauseIcon.style.display = 'none';
            } else {
                playIcon.style.display = 'none';
                pauseIcon.style.display = 'block';
            }
        });

        playPauseButton.addEventListener('mouseleave', function() {
            playIcon.style.display = 'none';
            pauseIcon.style.display = 'none';
        });

        // Show the pause button when the video is playing by default
        playIcon.style.display = 'none';
